package com.lym.twogoods.bean;

/**
 * <p>
 * 	图片缩略图信息实体类
 * </p>
 * 
 * @author 麦灿标
 * */
public class PictureThumbnailSpecification {

	/** 宽度 */
	private int width;
	
	/** 高度 */
	private int height;

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}
	
	
}
